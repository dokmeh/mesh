        <h4>Discription</h4>
        <p>
            Having turned the spotlight on the significance of Zayandeh Rood river in Isfahan, it is evident that it plays a pivotal role in this city.
            The Zayandeh Rood Hotel project, which was designed for the course (architectural design 3) at B.Arch. must have consider the vital situation of Zayandeh Rood river as the most paramount urban element. It was an important approach, not only for the urban view landscapes, but also for the skyline and catching splendid view of the project from Zayandeh Rood river.
            Under no circumstances should architects ignore the fact that various function spaces play a key role in the design process of hotels. In addition, the fulfillment of the design standards should be substantially prioritized simultaneously.
            On the following steps, according to the international design standards of hotels, architectural plans were designed meticulously.
            Moreover, designing an extreme opening in the central part of this building was a tactful strategy to fulfill better conditions of the project skyline.
            This hotel is located at the outside appearance of the street and has attempted to have extreme view of the surroundings from the perspective of hotel rooms.
