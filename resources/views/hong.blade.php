        <h4>Discription</h4>
        <p>
            Having turned the spotlight on the most prominent issues in this conceptual architecture competition, it was absolutely undeniable to consider the necessity of consuming renewable energy resources with the approach of archaeological architecture efficiently.
            On the one hand, it was highly recommended to consume renewable energy resources to meet the project requirements.
            On the other hand, it was considerably unavoidable that shortage of land for housing in Hong Kong was a major obstacle.
            Consequently, one of the paramount concerns of Hong Kong urban authorities was to implement some strategies which could maximize the number of people to live with more ideal living conditions with minimizing land use simultaneously.
            After considering the statement above, the strong solution to tackle this huge problem was to design a super skyscraper with approach of archaeological architecture.
            From the design team perspective, the main concept of the skyscraper design would be classified as follows:
            1-	How to circulate people with fully functional ramps.
            2-	How to design and locate some sidewalks at heights.
            In addition, it was anticipated consuming the solar power, the wind power, and the energy from moving people.
            The whole design process was resulted in designing perfect urban quarters which could efficiently meet demands of people at heights by bringing all the welfare conditions together.
            Finally, this design approach contributed to 7 separate quarters which were located on the top of each other.

