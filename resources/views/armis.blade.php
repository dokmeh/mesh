        <h4>Discription</h4>
        <p>
            The Relaxation Center is located in a peaceful area of the Shahrak-e Gharb region of Tehran. The client of this project was inclined to have a relaxing and pleasant place for the users of this building.
            The primary building was relatively an old villa which was constructed during the late Pahlavi dynasty. Taking the building original structure into consideration was absolutely a significant criterion.
            In addition, it was highly reinforced in order to have more functional performance.
            Around the building was completely excavated for the purpose that the sufficient daylight as an important factor can reach the basement floor which was considered to be the Book Café in design phase.
            An excavation of an eastern part of the basement floor has provided an opportunity to have a small-scale cinema with height of 6.5 meters.
            The varied functions such as the yoga salons, the consultation rooms, the VIP consultation room, the office rooms and etc were efficiently and meticulously brought together in the upper floors.
            The previous façade of the building was scheduled for demolition. Consequently, the final façade was creatively substituted for the old one according to the minimalist design approach

