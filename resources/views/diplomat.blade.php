            The Diplomat Multifunctional Complex is located in the Pardisan suburb in south of Qom.
            The special definition of land use in Pardisan suburb which is reputed to be as a mixed-use land is a new and significant challenge in the design method of this building.
            The building functions are categorized as following:
            1-	The ground floor: Commercial
            2-	The first and second floors: Administrative
            3-	The upper 3 floors above: Residential
