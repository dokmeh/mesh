        <h4>Discription</h4>
        <p>
            The design process was carried out in close consultation with the client who was highly determined to define a unique entrance to a recreational complex located in the forests adjacent to the Rezvanshahr town.
            From the point of view of the design team, it was absolutely essential to focus on unifying the entrance and all the bridges which were in close relationship with this area.
            The division of pedestrian and vehicle access to the bridge with its box shape played a fundamental role in the design process. Not only did this procedure control the arrival and departure access to the recreational area, but also it facilitated the arrival at forest from the alongside street.
            It was anticipated that self-propelled landscape in the project was a prominent strategy which should be implemented for the purpose that the bridge structure would be significantly in harmony with the surrounding nature.

